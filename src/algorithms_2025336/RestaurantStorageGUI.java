    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed

        try {
            String searchName = searchField.getText().trim();
            FoodItem foundFood = storage.searchItem(searchName);

            if (foundFood != null) {
                displayArea.append("Search Result:\n");
                displayArea.append(foundFood.toString() + "\n");
            } else {
                displayArea.append("No food item found with name: " + searchName + "\n");
            }

            displayArea.append("-----------------------------------\n");
        } catch (Exception e) {
            displayArea.append(e.getMessage() + "\n\n");
        }
    }//GEN-LAST:event_searchActionPerformed

