    private FoodItem createFoodItem(String selectedFood, int weight, LocalDate bestBeforeDate) {
    switch (selectedFood) {
        case "Burger":
            return new Burger(weight, bestBeforeDate);
        case "Pizza":
            return new Pizza(weight, bestBeforeDate);
        case "Fries":
            return new Fries(weight, bestBeforeDate);
        case "Sandwich":
            return new Sandwich(weight, bestBeforeDate);
        case "Hotdog":
            return new Hotdog(weight, bestBeforeDate);
        default:
            throw new IllegalArgumentException("Invalid food type selected.");
    }
}
    
    private void displayCurrentFoods() {
    displayArea.append("Current Food Items in Storage:\n");

    java.util.List<FoodItem> foods = storage.displayItems();

    if (foods.isEmpty()) {
        displayArea.append("Storage is empty.\n");
    } else {
        int i = 1;
        for (FoodItem food : foods) {
            displayArea.append(i + ". " + food.toString() + "\n");
            i++;
        }
    }

    displayArea.append("-----------------------------------\n");
}
    
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

            try {
                String selectedFood = foodTypeCombo.getSelectedItem().toString();
                int weight = Integer.parseInt(weightField.getText().trim());
                LocalDate bestBeforeDate = LocalDate.parse(dateField.getText().trim());

                String selectedMode = modeCombo.getSelectedItem().toString();
                if (selectedMode.equals("STACK")) {
                    storage.setMode(StorageMode.STACK);
                } else {
                    storage.setMode(StorageMode.QUEUE);
                }

                FoodItem food = createFoodItem(selectedFood, weight, bestBeforeDate);
                storage.addItem(food);

                displayArea.append(food.toString() + " has been added to storage.\n");
                displayArea.append("Current mode: " + storage.getMode() + "\n");

                if (!storage.isEmpty()) {
                    displayArea.append("Top item: " + storage.peekItem().toString() + "\n");
                }

                displayArea.append("-----------------------------------\n");
                displayCurrentFoods();

                weightField.setText("");
                dateField.setText("");

            } catch (NumberFormatException e) {
                displayArea.append("Weight must be a valid integer.\n\n");
            } catch (java.time.format.DateTimeParseException e) {
                displayArea.append("Date must be in YYYY-MM-DD format.\n\n");
            } catch (Exception e) {
                displayArea.append(e.getMessage() + "\n\n");
            }
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed

            try {
                FoodItem removedFood = storage.removeItem();
                displayArea.append("Removed: " + removedFood.getName() + "\n");

                if (!storage.isEmpty()) {
                    displayArea.append("Top item now: " + storage.peekItem().toString() + "\n");
                } else {
                    displayArea.append("Storage is now empty.\n");
                }

                displayArea.append("-----------------------------------\n");
                displayCurrentFoods();

            } catch (Exception e) {
                displayArea.append(e.getMessage() + "\n\n");
            }
    }//GEN-LAST:event_removeButtonActionPerformed

